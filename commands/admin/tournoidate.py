class TournoiDateModal(discord.ui.Modal, title="ğŸ“… Modifier le tournoi"):

    date = discord.ui.TextInput(
        label="Date du tournoi",
        placeholder="JJ/MM/AAAA HH:MM",
        required=True
    )

    lieu = discord.ui.TextInput(
        label="Lieu du tournoi",
        placeholder="Ex: Paris / Discord / Salle XYZ",
        required=True,
        max_length=100
    )

    async def on_submit(self, interaction: discord.Interaction):
        try:
            dt = datetime.strptime(self.date.value, "%d/%m/%Y %H:%M")
        except ValueError:
            return await interaction.response.send_message(
                "âŒ Format invalide.\nUtilise **JJ/MM/AAAA HH:MM**",
                ephemeral=True
            )

        supabase.table("tournoi_info").upsert({
            "id": 1,
            "prochaine_date": dt.isoformat(),
            "lieu": self.lieu.value
        }).execute()

        await interaction.response.send_message(
            "âœ… **Tournoi mis Ã  jour avec succÃ¨s**",
            ephemeral=True
        )


