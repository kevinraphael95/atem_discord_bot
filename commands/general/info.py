# ­ЪДа Cog principal
# РћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђ
class InfoCog(commands.Cog):
    """
    Commande !info Рђћ Affiche les nouveaut├Еs et derniers changements du bot.
    """

    def __init__(self, bot: commands.Bot):
        self.bot = bot

    @commands.command(
        name="info",
        help="Affiche les nouveaut├Еs et derniers changements du bot."
    )
    @commands.cooldown(rate=1, per=3, type=commands.BucketType.user)  # ­ЪДі Anti-spam : 1 appel / 3s / utilisateur
    async def info(self, ctx: commands.Context):
        """Commande principale qui envoie un embed avec les nouveaut├Еs."""
        prefix = ctx.prefix  # R├Еcup├еre le pr├Еfixe dynamique utilis├Е par le bot

        changelog_lines = [
            f"Рђб Commande $question :\n"
            f"- Commande maintenant multijoueur\n"
            f"- De meilleures propositions de noms quand la carte n'a aucun arch├Еtype"
        ]

        embed = discord.Embed(
            title="­ЪЏа№ИЈ Derniers changements",
            color=discord.Color.blue()
        )
        embed.description = "\n".join(changelog_lines)
        embed.set_footer(text="Merci d'utiliser le bot !")

        await ctx.send(embed=embed)

# РћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђ
# ­Ъћї Setup du Cog
# РћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђ
async def setup(bot: commands.Bot):
    cog = InfoCog(bot)
    for command in cog.get_commands():
        if not hasattr(command, "category"):
            command.category = "G├Еn├Еral"
    await bot.add_cog(cog)
