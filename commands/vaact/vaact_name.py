        pseudos = []
        reader = csv.reader(io.StringIO(response.text))
        next(reader, None)  # ignore l'en-tÃªte
        for row in reader:
            if len(row) >= 3 and row[2].strip():
                pseudos.append(row[2].strip())  # 3áµ‰ colonne = "Joueur"
        return pseudos

