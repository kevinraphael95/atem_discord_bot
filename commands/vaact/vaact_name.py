    def get_vaact_pseudos(self) -> list[str]:
        """RÃ©cupÃ¨re la liste des pseudos VAACT depuis la colonne 'Joueur' du CSV"""
        url = os.getenv("VAACT_CLASSEMENT_SHEET")
        response = requests.get(url)
