# ­ЪЌё№ИЈ Configuration SQLite
# РћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђ
DB_PATH = "database/tournoi.db"
os.makedirs("database", exist_ok=True)

def get_db():
    return sqlite3.connect(DB_PATH)

def init_db():
    conn = get_db()
    cursor = conn.cursor()
    cursor.execute("""
    CREATE TABLE IF NOT EXISTS tournoi_info (
        id INTEGER PRIMARY KEY,
        prochaine_date TEXT,
        lieu TEXT
    )
    """)
    cursor.execute("SELECT COUNT(*) FROM tournoi_info")
    if cursor.fetchone()[0] == 0:
        cursor.execute("INSERT INTO tournoi_info (id, prochaine_date, lieu) VALUES (1, NULL, NULL)")
    conn.commit()
    conn.close()

