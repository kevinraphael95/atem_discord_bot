class TournoiCommand(commands.Cog):
    """ğŸ“Œ Affiche la date et le lieu du prochain tournoi."""

    def __init__(self, bot: commands.Bot):
        self.bot = bot

    @commands.command(
        name="tournoi",
        help="ğŸ“… Affiche la date et le lieu du prochain tournoi VAACT.",
        description="RÃ©cupÃ¨re la date et le lieu depuis Supabase."
    )
    @commands.cooldown(rate=1, per=5, type=commands.BucketType.user)
    async def tournoi(self, ctx: commands.Context):
        """Commande principale !tournoi"""
        try:
            # âœ… Lecture de la seule ligne (un seul tournoi possible)
            result = supabase.table("tournoi_info").select("prochaine_date, lieu").execute()
            data = result.data
        except Exception as e:
            print(f"[ERREUR SUPABASE] {e}")
            await safe_send(ctx, "âŒ Impossible de se connecter Ã  Supabase.")
            return

        if not data or not data[0].get("prochaine_date"):
            await safe_send(ctx, "ğŸ“­ Aucun tournoi prÃ©vu pour le moment.")
            return

        # Formatage de la date
        iso_date = data[0]["prochaine_date"]
        lieu = data[0].get("lieu", "Non renseignÃ©")
        try:
            dt = datetime.fromisoformat(iso_date)
            date_formatee = dt.strftime('%d %B %Y Ã  %Hh%M')
        except Exception:
            date_formatee = iso_date  # fallback brut

        # Embed principal
        embed = discord.Embed(
            title="ğŸ“… Prochain tournoi VAACT",
            description=(
                f"ğŸ“† **Date** : {date_formatee}\n"
                f"ğŸ“ **Lieu** : {lieu}"
            ),
            color=discord.Color.gold()
        )

        # Ajouter bouton pour le lien CSV si dÃ©fini
        view = None
        if SHEET_CSV_URL:
            class DeckButton(discord.ui.View):
                def __init__(self):
                    super().__init__()
                    self.add_item(discord.ui.Button(
                        label="ğŸ“‹ Voir les decks",
                        url=SHEET_CSV_URL
                    ))
            view = DeckButton()

        await safe_send(ctx, embed=embed, view=view)

