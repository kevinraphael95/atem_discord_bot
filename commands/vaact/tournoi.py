# ­ЪДа Cog principal
# РћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђРћђ
class TournoiCommand(commands.Cog):
    """­ЪЊї Affiche la date et le lieu du prochain tournoi."""

    def __init__(self, bot: commands.Bot):
        self.bot = bot
        init_db()

    @commands.command(
        name="tournoi",
        help="­ЪЊЁ Affiche la date et le lieu du prochain tournoi VAACT.",
        description="R├Еcup├еre la date et le lieu depuis SQLite locale."
    )
    @commands.cooldown(rate=1, per=5, type=commands.BucketType.user)
    async def tournoi(self, ctx: commands.Context):
        conn = get_db()
        cursor = conn.cursor()
        cursor.execute("SELECT prochaine_date, lieu FROM tournoi_info WHERE id = 1")
        row = cursor.fetchone()
        conn.close()

        if not row or not row[0]:
            await safe_send(ctx, "­ЪЊГ Aucun tournoi pr├Еvu pour le moment.")
            return

        dt = datetime.fromisoformat(row[0])
        mois = MOIS_FR[dt.month - 1]
        date_formatee = f"{dt.day} {mois} {dt.year} ├а {dt.hour:02d}h{dt.minute:02d}"
        lieu = row[1] or "Non renseign├Е"

        embed = discord.Embed(
            title="­ЪЈє Prochain tournoi VAACT",
            description=f"­ЪЊє **Date** : {date_formatee}\n­ЪЊЇ **Lieu** : {lieu}",
            color=discord.Color.gold()
        )

        files = []
        if os.path.exists(VAACT_LOGO_PATH):
            file = discord.File(VAACT_LOGO_PATH, filename="vaact_logo.png")
            embed.set_thumbnail(url="attachment://vaact_logo.png")
            files.append(file)

        await safe_send(ctx, embed=embed, files=files)

