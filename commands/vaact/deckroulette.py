class DeckRoulette(commands.Cog):
    """
    Commande !deckroulette â€” Propose un deck custom alÃ©atoire Ã  un joueur
    """

    def __init__(self, bot: commands.Bot):
        self.bot = bot

    @commands.command(
        name="deckroulette",
        help="Donne un deck custom alÃ©atoire Ã  jouer.",
        description="Choisit au hasard un deck dans la liste des decks custom et l'affiche."
    )
    async def deckroulette(self, ctx: commands.Context):
        try:
            decks_data = load_decks()  # On charge le JSON complet
            
            # decks_data est attendu au format dict, par exemple :
            # { "deck1": {"deck": [...], "astuces": "..."},
            #   "deck2": {...}, ... }
            
            if not decks_data:
                await safe_send(ctx.channel, "âŒ Aucun deck disponible dans la base.")
                return

            deck_name = random.choice(list(decks_data.keys()))
            deck_info = decks_data[deck_name]

            deck_list = deck_info.get("deck", [])
            astuces = deck_info.get("astuces", "Aucune astuce disponible.")

            # Format du deck en liste
            if isinstance(deck_list, list):
                deck_text = "\n".join(f"â€¢ {card}" for card in deck_list)
            else:
                deck_text = str(deck_list)

            embed = discord.Embed(
                title=f"ğŸ² Deck alÃ©atoire : {deck_name}",
                color=discord.Color.green()
            )
            embed.add_field(name="ğŸ“˜ Deck", value=deck_text, inline=False)
            embed.add_field(name="ğŸ’¡ Astuces", value=astuces, inline=False)

            await safe_send(ctx.channel, embed=embed)

        except Exception as e:
            print(f"[ERREUR deckroulette] {e}")
            await safe_send(ctx.channel, "âŒ Une erreur est survenue lors du choix du deck.")

