class Randeck(commands.Cog):
    """
    Commande !randeck â€” Tire un deck alÃ©atoire Ã  jouer
    """

    def __init__(self, bot: commands.Bot):
        self.bot = bot

    @commands.command(
        name="randeck",
        aliases=["deckroulette"],
        help="Tire un deck custom alÃ©atoire Ã  jouer.",
        description="Choisis un deck alÃ©atoire parmi tous les decks custom enregistrÃ©s."
    )
    async def randeck(self, ctx: commands.Context):
        """Commande principale pour afficher un deck random."""
        try:
            data = load_data()
            decks = []

            for saison, persos in data.items():
                for duelliste, infos in persos.items():
                    deck_data = infos.get("deck", {})

                    if not isinstance(deck_data, dict):
                        continue

                    for niveau, contenus in deck_data.items():
                        if isinstance(contenus, dict):
                            for type_deck, lien in contenus.items():
                                decks.append((saison, duelliste, niveau, type_deck, lien))
                        elif isinstance(contenus, str):
                            decks.append((saison, duelliste, niveau, "Deck", contenus))

            if not decks:
                return await safe_send(ctx, "âŒ Aucun deck n'est disponible.")

            saison, duelliste, niveau, type_deck, lien = random.choice(decks)

            # â”€ Embed stylÃ© â”€
            embed = discord.Embed(
                title="ğŸ² Deck AlÃ©atoire TirÃ© !",
                color=discord.Color.random()
            )
            embed.add_field(
                name="ğŸ‘¤ Duelliste",
                value=f"**{duelliste}** *(Saison {saison})*",
                inline=False
            )
            embed.add_field(name="ğŸšï¸ Niveau", value=niveau, inline=True)
            embed.add_field(name="ğŸ“˜ Type", value=type_deck, inline=True)
            embed.add_field(name="ğŸ”— Deck", value=lien, inline=False)

            embed.set_footer(
                text=f"TirÃ© par {ctx.author.display_name}",
                icon_url=ctx.author.display_avatar.url
            )

            await safe_send(ctx.channel, embed=embed)

        except Exception as e:
            print(f"[ERREUR randeck] {e}")
            await safe_send(ctx.channel, "âŒ Une erreur est survenue lors du tirage du deck.")

