class DeckCommand(commands.Cog):
    """
    Commande /deck et !deck â€” Affiche un deck Ã  partir dâ€™un URL ydke:// ou dâ€™un fichier .ydk
    """
    def __init__(self, bot: commands.Bot):
        self.bot = bot

    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    # ğŸ”¹ Fonction interne commune
    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    async def _process_deck(
        self,
        interaction_or_ctx,
        url: str = None,
        file: discord.Attachment = None,
        stacked: bool = False
    ):
        start_time = datetime.utcnow()
        try:
            if url:
                deck = parse_url(url)
            elif file:
                if not file.filename.endswith(".ydk"):
                    await safe_edit(interaction_or_ctx, "âŒ Le fichier doit avoir lâ€™extension .ydk !")
                    return
                content = await file.read()
                deck = parse_ydk_file(BytesIO(content))
            else:
                await safe_edit(interaction_or_ctx, "âŒ Vous devez fournir un URL ou un fichier .ydk.")
                return

            if not (deck.main or deck.extra or deck.side):
                await safe_edit(interaction_or_ctx, "âŒ Votre deck est vide.")
                return

            out_url = to_url(deck)
            out_file = typed_deck_to_ydk(deck)
            embed = generate_deck_embed(deck, stacked, out_url)
            attachment = discord.File(BytesIO(out_file.encode("utf-8")), filename="deck.ydk")
            view = DeckButtonView(out_url)

            await safe_edit(interaction_or_ctx, embed=embed, files=[attachment], view=view)
            latency = (datetime.utcnow() - start_time).total_seconds() * 1000
            return latency

        except Exception as e:
            await safe_edit(interaction_or_ctx, f"âŒ Une erreur est survenue : {e}")

    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    # ğŸ”¹ Commande SLASH
    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    @app_commands.command(
        name="deckk",
        description="Affiche un deck Ã  partir dâ€™un URL ydke:// ou dâ€™un fichier .ydk"
    )
    @app_commands.checks.cooldown(1, 5.0, key=lambda i: i.user.id)
    async def slash_deck(
        self,
        interaction: discord.Interaction,
        url: str = None,
        file: discord.Attachment = None,
        stacked: bool = False
    ):
        """Commande slash principale."""
        await interaction.response.defer()
        await self._process_deck(interaction, url=url, file=file, stacked=stacked)

    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    # ğŸ”¹ Commande PREFIX
    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    @commands.command(name="deckk")
    @commands.cooldown(1, 5.0, commands.BucketType.user)
    async def prefix_deck(
        self,
        ctx: commands.Context,
        url: str = None,
        file: discord.Attachment = None,
        stacked: bool = False
    ):
        """Commande prÃ©fixe principale."""
        await self._process_deck(ctx, url=url, file=file, stacked=stacked)

