class QuizSelect(Select):
    def __init__(self, parent_view: "QuizView", options_list: List[str], correct_answer: str):
        self.parent_view = parent_view
        self.correct_answer = correct_answer
        select_options = [discord.SelectOption(label=opt, value=opt) for opt in options_list]
        super().__init__(placeholder="Choisis la bonne rÃ©ponse", options=select_options, max_values=1)

    async def callback(self, interaction: discord.Interaction):
        selected = self.values[0]
        if selected == self.correct_answer:
            content = f"âœ… Bravo ! **{selected}** est la bonne rÃ©ponse."
        else:
            content = f"âŒ Mauvaise rÃ©ponse... La bonne rÃ©ponse Ã©tait **{self.correct_answer}**."

        self.parent_view.clear_items()
        await interaction.response.defer()  # âš ï¸ NÃ©cessaire
        await safe_edit(interaction.message, content=content, embed=None, view=None)
        self.parent_view.stop()

