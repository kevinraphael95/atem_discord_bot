async def fetch_meta_cards(limit: int = 10) -> list[dict]:
    """
    R√©cup√®re une s√©lection de cartes 'META' actuelles.
    (L‚ÄôAPI YGOPRODeck ne fournit pas de taux d‚Äôutilisation officiel, donc on simule
     un classement bas√© sur les ATK les plus √©lev√©es pour le visuel.)
    """
    async with aiohttp.ClientSession() as session:
        url = "https://db.ygoprodeck.com/api/v7/cardinfo.php?language=fr&sort=atk&misc=yes"
        async with session.get(url) as resp:
            if resp.status != 200:
                return []

            data = await resp.json()
            cards = data.get("data", [])
            if not cards:
                return []

            # Trier par ATK d√©croissante
            cards = sorted(cards, key=lambda c: c.get("atk", 0) or 0, reverse=True)

            # Simuler un taux d‚Äôutilisation d√©croissant
            for i, c in enumerate(cards[:limit]):
                c["usage_rate"] = round(100 - (i * (100 / limit)), 1)

            return cards[:limit]
